# Пользователь вводит числа, пока не введет stop и подсчитать сумму введенных чисел.
# Сделать обработку и выход из цикла с помощью исключений.

num = 0
sum = 0
try:
    while num != 'stop':
        sum += int(num)
        num = input('Ведите число: ')
except:
    print("Строка содержит буквы")
else:
    print (f"сумма введеных чисел равна {sum}")
finally:
    print("Конец программы!")


