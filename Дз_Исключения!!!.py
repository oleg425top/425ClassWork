# Пользователь вводит сумму в рублях, необходимо написать программу, которая переводит ее в доллары
# и выводит на экран. Все обработки сделать с помощью исключений.
#
try:
    rub = int(input('Введите сумму в рублях: '))
    if rub < 0:
        raise TypeError('Число должно быть положительным')
    dollar = rub * 0.011
    print(f"у Вас {dollar:.1f} $")
except TypeError as err1:
    print(f"Error: {err1}")
except ValueError as err:
    print(f"Error:  {err}")
finally:
    print('Конец')



